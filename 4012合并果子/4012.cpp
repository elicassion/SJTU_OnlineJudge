using namespace std;
int sum=0,N;
priority_queue<int,vector<int>,greater<int> > q;
int main()
{
	cin>>N;
	for(int i=1;i<=N;i++)
	{
		int t;
		cin>>t;
		q.push(t);
	}
	while(!q.empty())
	{
		int t=q.top();
		int s;
		q.pop();
		if(!q.empty())
		{
			s=q.top();
			q.pop();
			sum+=s+t;
		}
		else break;
		q.push(s+t);
	}
	cout<<sum<<endl;
	return 0;
}
